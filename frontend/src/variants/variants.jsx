// Helper function to generate years for dropdown (current year and 5 years back)
export const generateYears = () => {
  const currentYear = new Date().getFullYear();
  const years = [];
  for (let i = 0; i < 6; i++) {
    years.push(currentYear - i);
  }
  return years;
};

export const years = generateYears();